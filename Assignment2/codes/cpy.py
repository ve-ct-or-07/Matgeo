from ctypes import *
cpy=CDLL('./cpy.so')
x1=3
y1=0
x2=0
y2=5
div=cpy.div
sub=cpy.sub
mod=cpy.mod
div.restype=c_float
sub.restype=c_float
mod.restype=c_float
q=mod(c_float(x1),c_float(y1))
r=mod(c_float(x2),c_float(y2))
d=sub(c_float(q),c_float(r))
k=div(c_float(d))
print("k=",k)
print("Q=(3,",k,")")
print("R=(",k,",5)")
